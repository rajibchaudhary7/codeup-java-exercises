public class StringExercise {
    public static void main(String[] args) {
        String education = "We don't need no education\n" +
                "We don't need no thought control";
        String checkThis = "Check \"this\" out!, \"s inside of \"s!";
        String mainDrive = "In windows, the main drive is usually C:\\";
        String backSlashes = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";

        System.out.println(education + "\n");
        System.out.println(checkThis + "\n");
        System.out.println(mainDrive + "\n");
        System.out.println(backSlashes);
    }
}
